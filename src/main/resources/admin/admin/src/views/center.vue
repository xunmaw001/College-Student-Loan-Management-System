      <el-col :span="12">
        <el-form-item   v-if="flag=='xuexiao'"  label="学校电话" prop="xuexiaodianhua">
          <el-input v-model="ruleForm.xuexiaodianhua"               placeholder="学校电话" clearable></el-input>
